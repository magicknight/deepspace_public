import torch
from torch import nn
from deepspace.graphs.layers.transformer.layers import Attention, MAttention, PreNorm, FeedForward


class Decoder(nn.Module):
    def __init__(self, dim, depth, heads, dim_head, mlp_dim, dropout=0.):
        super().__init__()
        self.layers = nn.ModuleList([])
        for _ in range(depth):
            self.layers.append(nn.ModuleList([
                PreNorm(dim, Attention(dim, heads=heads, dim_head=dim_head, dropout=dropout)),
                PreNorm(dim * 3, MAttention(dim, heads=heads, dim_head=dim_head, dropout=dropout)),
                PreNorm(dim, FeedForward(dim, mlp_dim, dropout=dropout))
            ]))

    def forward(self, x, m):
        for attn, mattn, ff in self.layers:
            x = attn(x) + x
            x = mattn(torch.cat([x, m, m], dim=-1)) + x
            x = ff(x) + x
        return x


if __name__ == "__main__":
    import torch
    from torchinfo import summary
    model = Decoder(dim=768, depth=12, heads=12, dim_head=64, mlp_dim=3072, dropout=0.1)
    summary(model, input_size=[(6, 577, 768), (6, 577, 768)])
    test_data = torch.randn(6, 577, 768)
    out = model(test_data, test_data)
    print(out.shape, out.min(), out.max(), out.mean())

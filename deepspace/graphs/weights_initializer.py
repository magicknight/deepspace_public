"""
A file for all models' weight initialization functions
"""




import torch
from torch import nn
import numpy as np
import math
def weights_init(m):
    classname = m.__class__.__name__
    if classname.find('Conv') != -1:
        m.weight.data.normal_(0.0, 0.02)
    elif classname.find('BatchNorm') != -1:
        m.weight.data.normal_(1.0, 0.02)
        m.bias.data.fill_(0)


def weights_init_normal(m):
    """
    Initialize the weights of Convolution2D and BatchNorm2D with normal.
    :param m:
    :return:
    """
    if isinstance(m, nn.Conv2d):
        m.weight.data.normal_(0.0, 0.02)
    elif isinstance(m, nn.BatchNorm2d):
        m.weight.data.normal_(1.0, 0.02)
        m.bias.data.fill_(0)


def init_model_weights(m):
    # initialize
    for m in m.modules():
        if isinstance(m, nn.Conv2d):
            n = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            m.weight.data.normal_(0, math.sqrt(2. / n))
        elif isinstance(m, nn.BatchNorm2d):
            m.weight.data.fill_(1)
            m.bias.data.zero_()
        elif isinstance(m, nn.Linear):
            m.bias.data.zero_()


def xavier_weights(m):
    '''apply xavier initialization on model weights'''
    if hasattr(m, 'weight'):
        if len(m.weight.shape) > 1:
            torch.nn.init.xavier_normal_(m.weight)


def kaiming_weights_v2(m):
    '''apply kaiming initialization on model weights'''
    if hasattr(m, 'weight'):
        if len(m.weight.shape) > 1:
            torch.nn.init.kaiming_normal_(m.weight, nonlinearity='relu')
